package org.example.week1.d4.e4;

public class E4CreatingVariablesAndPrinting {
    public static void main(String[] args) {

        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("##");
        System.out.println("#");
        // The pattern should be:
        // *
        // **
        // ***
        // ##
        // #

        // Note: Each System.out.println statement will create a new line

    }
}
